from __future__ import annotations

"""
Kalshi Market Scanner — Fetches KXBTC15M contracts and detects edge.

Scans active 15-minute BTC contracts every 45 seconds,
compares our probability estimate to Kalshi's implied odds,
and generates trade signals when edge >= min_edge_cents.
"""

import asyncio
import re
import time
import logging
from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Optional

import httpx
from cryptography.hazmat.primitives import hashes, serialization
from cryptography.hazmat.primitives.asymmetric import padding, utils
import base64
import hashlib

logger = logging.getLogger("kalshi_scanner")


@dataclass
class KalshiContract:
    """Parsed Kalshi KXBTC15M contract."""
    ticker: str
    strike_price: float
    close_time: datetime
    yes_price: int          # cents (0-100)
    no_price: int           # cents
    minutes_to_close: float
    volume: int = 0
    open_interest: int = 0

    @property
    def implied_probability(self) -> float:
        """Market-implied probability from YES price."""
        return self.yes_price / 100.0


@dataclass
class TradeSignal:
    """A trade signal generated by edge detection."""
    ticker: str
    strike_price: float
    side: str               # "yes" or "no"
    our_probability: float
    kalshi_implied: float
    edge_cents: float
    minutes_to_close: float
    indicators: dict
    probability_details: dict
    timestamp: float = field(default_factory=time.time)


# Ticker formats:
# 15-min: KXBTC15M-26APR070715-T68299.99
# Hourly: KXBTCHR-26APR0700-T68299.99
# Daily:  KXBTCD-26APR07-T68299.99
# Generic: KX<ASSET><PERIOD>-<DATE_TIME>-T<STRIKE>
TICKER_PATTERN = re.compile(
    r"KX[A-Z]+-(\d{2})([A-Z]{3})(\d{2,6})-T([\d.]+)"
)

MONTH_MAP = {
    "JAN": 1, "FEB": 2, "MAR": 3, "APR": 4, "MAY": 5, "JUN": 6,
    "JUL": 7, "AUG": 8, "SEP": 9, "OCT": 10, "NOV": 11, "DEC": 12,
}


def parse_ticker(ticker: str) -> Optional[tuple[float, datetime]]:
    """
    Parse Kalshi crypto ticker into (strike_price, close_time).

    Formats:
      KXBTC15M-26APR070715-T68299.99  → 15-min (DDHHM M = 6 digits)
      KXBTCHR-26APR0707-T68299.99     → Hourly (DDHHMM = 4 digits)
      KXBTCD-26APR07-T68299.99        → Daily  (DD = 2 digits)
    """
    m = TICKER_PATTERN.match(ticker)
    if not m:
        return None

    year_suffix = m.group(1)    # "26"
    month_str = m.group(2)      # "APR"
    time_str = m.group(3)       # varies: "070715", "0707", "07"
    strike_str = m.group(4)     # "68299.99"

    try:
        year = 2000 + int(year_suffix)
        month = MONTH_MAP.get(month_str, 0)
        if month == 0:
            return None

        if len(time_str) == 6:
            # 15-min: DDHHMM
            day = int(time_str[0:2])
            hour = int(time_str[2:4])
            minute = int(time_str[4:6])
        elif len(time_str) == 4:
            # Hourly: DDHH
            day = int(time_str[0:2])
            hour = int(time_str[2:4])
            minute = 0
        elif len(time_str) == 2:
            # Daily: DD (closes at end of day, assume 21:00 UTC / 5PM ET)
            day = int(time_str[0:2])
            hour = 21
            minute = 0
        else:
            # Try best guess
            day = int(time_str[0:2])
            hour = int(time_str[2:4]) if len(time_str) >= 4 else 21
            minute = int(time_str[4:6]) if len(time_str) >= 6 else 0

        close_time = datetime(year, month, day, hour, minute, tzinfo=timezone.utc)
        strike_price = float(strike_str)
        return strike_price, close_time
    except (ValueError, IndexError):
        return None


class KalshiScanner:
    """
    Scans Kalshi for active KXBTC15M contracts and detects trading edges.
    """

    # Map asset keywords in tickers to engine keys
    ASSET_MAP = {
        "BTC": "BTC",
        "ETH": "ETH",
    }

    def __init__(self, settings, engines: dict, database):
        """
        engines: dict mapping asset name → BTCEngine instance.
        e.g. {"BTC": btc_engine, "ETH": eth_engine}
        """
        self.settings = settings
        self.engines = engines
        self.btc_engine = engines.get("BTC")  # backward compat
        self.db = database
        self._running = False
        self._client: Optional[httpx.AsyncClient] = None
        self.last_scan_time: float = 0
        self.contracts_found: int = 0
        self.signals_today: int = 0
        self.series_counts: dict[str, int] = {}  # series → contract count from last scan
        self._discovered_series: list[tuple[str, str]] = []  # (series_ticker, asset_key)
        self._last_discovery: float = 0
        self._burst_scan_requested: bool = False

    def request_burst_scan(self):
        """Called by momentum detector to trigger immediate scan."""
        self._burst_scan_requested = True

    # ── Main Loop ──────────────────────────────────────────────

    async def start(self, signal_callback):
        """
        Start scanning loop. Calls signal_callback(TradeSignal) for each edge found.
        """
        self._running = True
        self._client = httpx.AsyncClient(timeout=20)

        assets = [k for k, e in self.engines.items() if e is not None]
        logger.info(f"Kalshi scanner started — scanning: {', '.join(assets)}")

        # Discover available series on first run
        await self._discover_series()

        while self._running:
            try:
                # Re-discover series every 30 min
                if time.time() - self._last_discovery > 1800:
                    await self._discover_series()

                any_ready = any(e.ready for e in self.engines.values() if e)
                if any_ready:
                    signals = await self._scan_once()
                    for sig in signals:
                        await signal_callback(sig)
                else:
                    logger.debug("No engines ready, skipping scan")
            except Exception as e:
                logger.error(f"Scan error: {e}", exc_info=True)

            # Shorter wait if burst scan was requested
            if self._burst_scan_requested:
                self._burst_scan_requested = False
                await asyncio.sleep(2)  # quick rescan after burst
            else:
                await asyncio.sleep(self.settings.kalshi_scan_interval_sec)

    async def stop(self):
        self._running = False
        if self._client:
            await self._client.aclose()

    # ── Series Discovery ───────────────────────────────────────

    async def _discover_series(self):
        """
        Build the list of crypto series to scan.
        Uses known series tickers and verifies which have active markets.
        """
        self._last_discovery = time.time()

        # All known crypto series we want to scan
        known_series = [
            ("KXBTC15M", "BTC"),
            ("KXBTCHR", "BTC"),
            ("KXBTCD", "BTC"),
            ("KXETH15M", "ETH"),
            ("KXETHHR", "ETH"),
            ("KXETHD", "ETH"),
        ]

        # Filter to engines we actually have running
        self._discovered_series = [
            (s, a) for s, a in known_series if self.engines.get(a)
        ]
        series_list = [f"{s}({a})" for s, a in self._discovered_series]
        logger.info(f"Scanning {len(self._discovered_series)} series: {', '.join(series_list)}")

    # ── Scanning ───────────────────────────────────────────────

    async def _scan_once(self) -> list[TradeSignal]:
        """Fetch active contracts for all discovered series and evaluate each."""
        all_contracts = []
        series_counts = []

        for series_ticker, asset_key in self._discovered_series:
            engine = self.engines.get(asset_key)
            if not engine or not engine.ready:
                series_counts.append(f"{series_ticker}=no_engine")
                self.series_counts[series_ticker] = 0
                continue
            contracts = await self._fetch_contracts(series_ticker)
            series_counts.append(f"{series_ticker}={len(contracts)}")
            self.series_counts[series_ticker] = len(contracts)
            for c in contracts:
                c._engine = engine
            all_contracts.extend(contracts)

        self.contracts_found = len(all_contracts)
        self.last_scan_time = time.time()

        logger.info(f"Scan: {', '.join(series_counts)} → {len(all_contracts)} total contracts")

        signals = []
        for contract in all_contracts:
            engine = getattr(contract, '_engine', self.btc_engine)
            signal = self._evaluate_contract(contract, engine)
            if signal:
                signals.append(signal)

        if signals:
            # Sort by edge (highest first) and limit to top 10
            signals.sort(key=lambda s: s.edge_cents, reverse=True)
            top_signals = signals[:10]
            logger.info(
                f"Found {len(signals)} signals from {len(all_contracts)} contracts "
                f"— sending top {len(top_signals)} to AI"
            )
            self.signals_today += len(top_signals)
            signals = top_signals
        else:
            logger.debug(f"No signals from {len(all_contracts)} contracts (all filtered)")

        return signals

    async def _fetch_contracts(self, series_ticker: str = "KXBTC15M") -> list[KalshiContract]:
        """Fetch active markets for a given series from Kalshi API."""
        try:
            path = "/trade-api/v2/markets"
            headers = self._sign_request("GET", path)

            resp = await self._client.get(
                f"{self.settings.kalshi_base_url}{path}",
                params={
                    "status": "open",
                    "series_ticker": series_ticker,
                    "limit": 100,
                },
                headers=headers,
            )
            resp.raise_for_status()
            data = resp.json()

            contracts = []
            now = datetime.now(timezone.utc)

            for market in data.get("markets", []):
                ticker = market.get("ticker", "")

                # Try to get strike/close from API fields first, fall back to ticker parsing
                strike_price = market.get("floor_strike")
                close_time_str = market.get("close_time", "")

                if strike_price and close_time_str:
                    try:
                        close_time = datetime.fromisoformat(close_time_str.replace("Z", "+00:00"))
                    except (ValueError, AttributeError):
                        parsed = parse_ticker(ticker)
                        if not parsed:
                            continue
                        strike_price, close_time = parsed
                else:
                    parsed = parse_ticker(ticker)
                    if not parsed:
                        continue
                    strike_price, close_time = parsed

                minutes_to_close = (close_time - now).total_seconds() / 60.0

                # Skip contracts too close to expiry or already expired
                if minutes_to_close < 2:
                    logger.debug(f"Skip {ticker}: too close to expiry ({minutes_to_close:.1f} min)")
                    continue
                # Skip contracts more than 48 hours out (too far)
                if minutes_to_close > 2880:
                    continue

                # Kalshi API v2 returns prices in dollars as strings
                # Try new format first (dollars), then fall back to old (cents)
                yes_bid_str = market.get("yes_bid_dollars", "")
                yes_ask_str = market.get("yes_ask_dollars", "")
                no_bid_str = market.get("no_bid_dollars", "")
                last_str = market.get("last_price_dollars", "")

                if yes_bid_str and float(yes_bid_str) > 0:
                    yes_price = int(float(yes_bid_str) * 100)
                elif yes_ask_str and float(yes_ask_str) > 0:
                    yes_price = int(float(yes_ask_str) * 100)
                elif last_str and float(last_str) > 0:
                    yes_price = int(float(last_str) * 100)
                else:
                    # Old API format (cents)
                    yes_price = market.get("yes_bid", 0) or market.get("last_price", 50)

                no_price = 100 - yes_price

                contracts.append(KalshiContract(
                    ticker=ticker,
                    strike_price=strike_price,
                    close_time=close_time,
                    yes_price=yes_price,
                    no_price=no_price,
                    minutes_to_close=minutes_to_close,
                    volume=market.get("volume", 0),
                    open_interest=market.get("open_interest", 0),
                ))

            return contracts

        except httpx.HTTPStatusError as e:
            if e.response.status_code == 401:
                self._auth_token = None  # force re-auth
                logger.warning("Kalshi auth expired, will re-auth next scan")
            else:
                logger.error(f"Kalshi API error: {e.response.status_code}")
            return []
        except Exception as e:
            logger.error(f"Failed to fetch Kalshi contracts: {e}")
            return []

    def _evaluate_contract(self, contract: KalshiContract, engine=None) -> Optional[TradeSignal]:
        """Evaluate a single contract for trading edge."""
        if engine is None:
            engine = self.btc_engine
        est = engine.estimate_probability(
            strike_price=contract.strike_price,
            minutes_to_close=contract.minutes_to_close,
        )

        # Skip if engine not ready (volatility not computed yet)
        # Returns -1.0 sentinel — old default of 0.5 caused phantom edges
        if est.probability < 0:
            logger.debug(f"Skip {contract.ticker}: engine not ready (no volatility data)")
            return None

        # Skip extreme probabilities — these are deep OTM contracts where
        # both us and Kalshi agree it's very unlikely. Any "edge" is noise.
        if est.probability > 0.90 or est.probability < 0.10:
            logger.debug(
                f"Skip {contract.ticker}: extreme prob {est.probability:.1%} "
                f"(strike={contract.strike_price:.0f}, {contract.minutes_to_close:.0f}min)"
            )
            return None

        # Only trade contracts in the 35-65¢ range — genuinely contested strikes.
        # Cheap contracts are longshots (16% win rate historically).
        # Expensive contracts have bad risk/reward.
        if contract.yes_price < 35 or contract.yes_price > 65:
            logger.debug(f"Skip {contract.ticker}: price {contract.yes_price}¢ outside 35-65¢ range")
            return None

        # Check YES side edge
        yes_edge = (est.probability - contract.implied_probability) * 100
        # Check NO side edge
        no_edge = ((1 - est.probability) - (contract.no_price / 100.0)) * 100

        min_edge = self.settings.min_edge_cents
        best_edge = max(yes_edge, no_edge)
        best_side = "yes" if yes_edge >= no_edge else "no"

        # Get vol model details for logging
        mkt_prob = contract.implied_probability
        our_vol = est.scaled_vol
        engine_dict = engine.to_dict()
        har_vol = engine_dict.get("har_vol_forecast", 0)
        jump_mult = engine_dict.get("jump_vol_multiplier", 1.0)
        jumps = engine_dict.get("jumps_last_hour", 0)

        # Log evaluation for all non-extreme contracts so we can see what's happening
        if best_edge >= 1.0:  # only log if at least 1¢ edge to avoid noise
            jump_tag = f" JUMP:{jump_mult:.1f}x" if jump_mult > 1.0 else ""
            logger.info(
                f"Edge {contract.ticker}: {best_side} {best_edge:+.1f}¢ "
                f"(prob={est.probability:.0%}, mkt={mkt_prob:.0%}, "
                f"HAR={har_vol:.3f}%, vol={our_vol:.3f}%, z={est.z_score:+.2f}, "
                f"{contract.minutes_to_close:.0f}min{jump_tag}) "
                f"{'→ SIGNAL' if best_edge >= min_edge else '→ below min'}"
            )

        signal = None

        if yes_edge >= min_edge:
            signal = TradeSignal(
                ticker=contract.ticker,
                strike_price=contract.strike_price,
                side="yes",
                our_probability=est.probability,
                kalshi_implied=contract.implied_probability,
                edge_cents=yes_edge,
                minutes_to_close=contract.minutes_to_close,
                indicators=engine.to_dict(),
                probability_details={
                    "base_prob": est.base_prob,
                    "momentum_adj": est.momentum_adj,
                    "rsi_adj": est.rsi_adj,
                    "bb_adj": est.bb_adj,
                    "funding_adj": est.funding_adj,
                    "z_score": est.z_score,
                    "distance": est.distance,
                    "scaled_vol": est.scaled_vol,
                },
            )
        elif no_edge >= min_edge:
            signal = TradeSignal(
                ticker=contract.ticker,
                strike_price=contract.strike_price,
                side="no",
                our_probability=1 - est.probability,
                kalshi_implied=contract.no_price / 100.0,
                edge_cents=no_edge,
                minutes_to_close=contract.minutes_to_close,
                indicators=engine.to_dict(),
                probability_details={
                    "base_prob": 1 - est.base_prob,
                    "momentum_adj": -est.momentum_adj,
                    "rsi_adj": -est.rsi_adj,
                    "bb_adj": -est.bb_adj,
                    "funding_adj": -est.funding_adj,
                    "z_score": -est.z_score,
                    "distance": -est.distance,
                    "scaled_vol": est.scaled_vol,
                },
            )

        if signal:
            # Record to database
            self.db.record_signal(
                ticker=signal.ticker,
                strike_price=signal.strike_price,
                side=signal.side,
                our_prob=signal.our_probability,
                kalshi_implied=signal.kalshi_implied,
                edge_cents=signal.edge_cents,
                indicators=signal.indicators,
            )

        return signal

    # ── Kalshi Per-Request Signing (RSA-PSS) ─────────────────

    def _load_private_key(self):
        """Load RSA private key from PEM file or env var (cached)."""
        if not hasattr(self, '_private_key') or self._private_key is None:
            try:
                import os
                # Option 1: Base64-encoded PEM (immune to Render newline mangling)
                b64_env = os.environ.get("KALSHI_PRIVATE_KEY_B64", "")
                if b64_env:
                    pem_bytes = base64.b64decode(b64_env)
                    self._private_key = serialization.load_pem_private_key(pem_bytes, password=None)
                    logger.info("Kalshi RSA key loaded from B64 env var")
                    return self._private_key

                # Option 2: Raw PEM string
                pem_env = os.environ.get("KALSHI_PRIVATE_KEY_PEM", "")
                if pem_env:
                    if "\\n" in pem_env:
                        pem_env = pem_env.replace("\\n", "\n")
                    self._private_key = serialization.load_pem_private_key(pem_env.encode(), password=None)
                    logger.info("Kalshi RSA key loaded from PEM env var")
                    return self._private_key

                # Option 3: PEM file
                key_path = self.settings.kalshi_private_key_path
                with open(key_path, "rb") as f:
                    self._private_key = serialization.load_pem_private_key(f.read(), password=None)
                logger.info("Kalshi RSA key loaded from file")
            except FileNotFoundError:
                logger.warning(f"RSA key not found at {self.settings.kalshi_private_key_path}")
                self._private_key = None
            except Exception as e:
                logger.error(f"Failed to load RSA key: {e}")
                self._private_key = None
        return self._private_key

    def _sign_request(self, method: str, path: str) -> dict:
        """
        Sign a Kalshi API request. Returns headers dict.
        Kalshi uses per-request RSA-PSS signing (no login endpoint).

        Message format: {timestamp_ms}{METHOD}{path_without_query}
        Algorithm: RSA-PSS with SHA256, MGF1(SHA256), salt=digest_length
        """
        if not self.settings.kalshi_api_key:
            return {}

        private_key = self._load_private_key()
        if not private_key:
            return {}

        timestamp_ms = str(int(time.time() * 1000))

        # Strip query params from path for signing
        path_no_query = path.split("?")[0]
        message = f"{timestamp_ms}{method.upper()}{path_no_query}".encode()

        signature = private_key.sign(
            message,
            padding.PSS(
                mgf=padding.MGF1(hashes.SHA256()),
                salt_length=padding.PSS.DIGEST_LENGTH,
            ),
            hashes.SHA256(),
        )
        sig_b64 = base64.b64encode(signature).decode()

        return {
            "KALSHI-ACCESS-KEY": self.settings.kalshi_api_key,
            "KALSHI-ACCESS-TIMESTAMP": timestamp_ms,
            "KALSHI-ACCESS-SIGNATURE": sig_b64,
        }

    async def _ensure_auth(self):
        """No-op — Kalshi uses per-request signing, no login needed."""
        pass

    # ── Serialization ──────────────────────────────────────────

    def to_dict(self) -> dict:
        return {
            "last_scan": self.last_scan_time,
            "contracts_found": self.contracts_found,
            "signals_today": self.signals_today,
            "running": self._running,
            "series_counts": dict(self.series_counts),
        }
